public class Ex16_ComparacaoDoisNumeros {
    public static void main(String[] args) {
        System.out.println("\n--- 16. Comparação Entre Dois Números ---");
        int a = 10, b = 20;
        if (a > b) System.out.println(a + " é maior que " + b);
        else if (a < b) System.out.println(a + " é menor que " + b);
        else System.out.println("São iguais");
    }
}