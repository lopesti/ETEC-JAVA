public class Ex05_DivisivelPor3e5 {
    public static void main(String[] args) {
        System.out.println("\n--- 5. Divisível por 3 e 5 ---");
        int numero = 15;
        if (numero % 3 == 0 && numero % 5 == 0) System.out.println("Divisível por 3 e 5");
        else System.out.println("Não é divisível pelos dois");
    }
}